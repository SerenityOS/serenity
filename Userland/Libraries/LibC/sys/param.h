#pragma once

#include <endian.h>
#include <limits.h>

#ifndef MIN
#    define MIN(a, b) (((a) < (b)) ? (a) : (b))
#endif

#ifndef MAX
#    define MAX(a, b) (((a) > (b)) ? (a) : (b))
#endif

#ifndef howmany
#    define howmany(x, y) (((x) + ((y) - 1)) / (y))
#endif
